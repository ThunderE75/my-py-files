# Write a python program that accepts two numbers
# and displays the sum, difference, multiplication, division of the two numbers.

a = input('Enter the first number: ')
b = input('Enter the second number: ')

c = int(a) + int(b)
print('Sum of the two numbers: ', c)
c = int(a)-int(b)
print('Difference of the two numbers: ', c)
c = int(a)*int(b)
print('Multiplication of the two numbers: ', c)
c = int(a)/int(b)
print('Division of the two numbers: ', c)
c = int(a)**int(b)
print('Exponent of the two numbers: ', c)
